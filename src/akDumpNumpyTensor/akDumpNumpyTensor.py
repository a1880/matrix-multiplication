def comment(s: str = ""):
    w(f"# {s}")


def get_term(name: str, a: np.array, 
             rows: int, cols: int, product: int, 
             transposed: bool = False) -> str:
    s = ""    
    idx = 0

    for row in range(rows):
        for col in range(cols):
            v = a[idx, product]

            if v != 0:
                v = int(v)
                lit = literal(name, row, col) if not transposed else literal(name, col, row)
                if s == "":
                    if v > 0:
                        s = lit
                    else:
                        s = f"- {lit}"
                elif v > 0: 
                    s += f" + {lit}"
                elif v < 0:
                    s += f" - {lit}"
                else:
                    assert False, f"Strange value! {v}"
            idx += 1

    return f"({s})"


def literal(name: str, row: int, col: int) -> str:
    return f"{name}{row+1}{col+1}"


def w(s: str = ""):
    wrf.write(f"{s}\n")
    print(s)


